package четырнадцатьДва;

import java.util.Arrays;
import java.util.List;

public class Main {
    public static void main(String[] args) {
        List<String> рыбы = Arrays.asList("лосось", "тунец", "форель", "лосось", "треска");

        Fish рыбаКоллекция = new Fish(рыбы);

        System.out.println("Исходная коллекция: " + рыбаКоллекция);

        рыбаКоллекция.удалитьДубликаты();
        System.out.println("Коллекция без дубликатов: " + рыбаКоллекция);

        рыбаКоллекция.сортировать();
        System.out.println("Отсортированная коллекция: " + рыбаКоллекция);

        рыбаКоллекция.заменитьРыбу("лосось", "скат");
        System.out.println("Коллекция после замены: " + рыбаКоллекция);

        рыбаКоллекция.поменятьМестамиРыб(0, 3);
        System.out.println("Коллекция после обмена: " + рыбаКоллекция);

        рыбаКоллекция.удалитьПоследнююРыбу();
        System.out.println("Коллекция после удаления последнего элемента: " + рыбаКоллекция);
    }
}


